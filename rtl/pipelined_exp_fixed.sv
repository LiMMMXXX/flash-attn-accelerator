// ==========================================================================
// pipelined_exp_fixed — 5级流水线定点 exp(x) 单元 ★核心数据通路
// ==========================================================================
// 作者:     Cowork 3P
// 日期:     2026-06-05
// 版本:     1.0
//
// 功能描述:
//   - 计算 exp(x), x ≤ 0 (softmax 输入经 max 减法后总是 ≤ 0)
//   - 算法: 范围缩减 + 4次 Horner 多项式近似
//   - 5 级流水线, 1 result/cycle 吞吐
//
// 数据格式:
//   输入:  Q8.24 定点有符号, x ∈ (-∞, 0]
//   输出:  Q8.24 定点有符号, y ∈ (0, 1]
//
// 接口: 流式 valid-ready 握手
//
// 关键设计决策:
//   - 选择4次多项式而非 LUT: 面积小 (~300 LUT + 4乘法器), 精度符合同等要求
//   - 5级流水线: Stage1(范围缩减)→2(r²)→3(Horner1)→4(Horner2)→5(重构)
//   - Q8.24 精度: 4次多项式的截断误差在 2^-20 以内
//
// 面积估算: ~4 DSP/LUT乘法器 + ~300 LUT (移位/加法逻辑)
//
// 参考:
//   - Online Normalizer: https://arxiv.org/abs/1805.02867
//   - 参考示例: 示例/flashattn-softmax-engine-main/rtl/pipelined_exp.sv
// ==========================================================================

`ifndef PIPELINED_EXP_FIXED_SV
`define PIPELINED_EXP_FIXED_SV

`timescale 1ns / 1ps

module pipelined_exp_fixed #(
    parameter int DATA_WIDTH = 32   // Q8.24 内部数据位宽
) (
    // ---- 时钟 & 复位 ----
    input  logic                         clk,
    input  logic                         rst_n,

    // ---- 数据输入 (Q8.24, x ≤ 0) ----
    input  logic signed [DATA_WIDTH-1:0] x_in,
    input  logic                         x_valid,

    // ---- 数据输出 (Q8.24, y ∈ (0, 1]) ----
    output logic signed [DATA_WIDTH-1:0] y_out,
    output logic                         y_valid
);

    // ======================================================================
    // Q8.24 定点常量 (在 flashattn_pkg 中定义, 此处在模块内重复声明)
    // ======================================================================
    // 注: 正式实现时从 flashattn_pkg 导入

    // ======================================================================
    // Stage 1: 范围缩减
    //   n = floor(x / ln(2)), r = x - n × ln(2)
    //   下溢检测: x < -88.7 → exp(x) → 0
    // ======================================================================
    // 信号: s1_n, s1_r, s1_valid, s1_underflow

    // ======================================================================
    // Stage 2: r² 计算
    //   r² = r × r (Q8.24 × Q8.24 → 取 Q8.24)
    // ======================================================================
    // 信号: s2_r, s2_r2, s2_n, s2_valid, s2_underflow

    // ======================================================================
    // Stage 3: Horner 第一步
    //   p = 1/2 + r × (1/6 + r × 1/24)
    // ======================================================================
    // 信号: s3_p, s3_r, s3_r2, s3_n, s3_valid, s3_underflow

    // ======================================================================
    // Stage 4: Horner 第二步 (最终)
    //   exp(r) = 1 + r + r² × p
    // ======================================================================
    // 信号: s4_exp_r, s4_n, s4_valid, s4_underflow

    // ======================================================================
    // Stage 5: 重构
    //   exp(x) = exp(r) >> (-n)    (n ≤ 0, 右移 |n| 位)
    // ======================================================================
    // 输出: y_out, y_valid

endmodule

`endif // PIPELINED_EXP_FIXED_SV
